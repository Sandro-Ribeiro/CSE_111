"""
A for loop iterates over a sequence, such as a list. This 
means a for loop causes the computer to repeatedly execute 
the statements in the body of the for loop, once for each 
element in the sequence
"""

def main():
    # Create a list of color names.
    colors = ["red", "orange", "yellow", "green", "blue"]
    # Use a for loop to print each element in the list.
    for color in colors:
        print(color)
        
# Call main to start this program.
if __name__ == "__main__":
    main()